class UsernameUpdate(BaseModel):
    """Request body for PUT /settings/username."""
    username: str


class PasswordUpdate(BaseModel):
    """Request body for PUT /settings/password."""
    current_password: str
    new_password: str


class RegisterRequest(BaseModel):
    """Request body for POST /auth/create-user."""
    username: str
    password: str


class LoginRequest(BaseModel):
    """Request body for POST /auth/login."""
    username: str
    password: str


class MultiUserModeRequest(BaseModel):
    """Request body for PUT /auth/multi-user-mode."""
    enabled: bool
    password: str | None = None


class SetAdminPasswordRequest(BaseModel):
    """Request body for directly setting the admin password."""
    password: str
    current_password: str | None = None


class TokenResponse(BaseModel):
    """Response body for login and auto-login."""
    token: str
    username: str


class CurrencyThresholdEntry(BaseModel):
    """A single currency threshold entry (part of a batch save)."""
    category_id: str
    min_count: int = Field(ge=1)
    days_window: int = Field(ge=1)


class CurrencyThresholdsSaveRequest(BaseModel):
    """Request body for PUT /currency/thresholds."""
    thresholds: list[CurrencyThresholdEntry]


class VisibilitySaveRequest(BaseModel):
    """Request body for PUT /settings/visibility."""
    page_visibility: str  # JSON-encoded dict
    column_visibility: str  # JSON-encoded dict


# ── Auth endpoints ──


@router.post("/auth/login", response_model=TokenResponse)
async def login(data: LoginRequest):
    """Authenticate a user and return a session token.

    Verifies the username and password against the ``users`` table.
    Returns a session token that must be passed as ``Authorization: Bearer <token>``
    on subsequent requests.
    """
    if not data.username.strip():
        raise HTTPException(status_code=400, detail="Username and password are required")
    if not data.password:
        raise HTTPException(status_code=400, detail="Username and password are required")

    conn = get_connection()
    try:
        user_row = conn.execute(
            "SELECT id, username, password FROM users WHERE username = ?",
            (data.username.strip(),),
        ).fetchone()

        if not user_row:
            raise HTTPException(status_code=403, detail="Invalid username or password")

        if not _verify_password(data.password, user_row["password"]):
            raise HTTPException(status_code=403, detail="Invalid username or password")

        token = _create_session(user_row["id"], conn=conn)
        conn.commit()
        return {"token": token, "username": user_row["username"]}
    finally:
        conn.close()


@router.post("/auth/create-user", response_model=TokenResponse)
async def create_user(data: RegisterRequest):
    """Create an additional (non-admin) user.

    This endpoint is only intended for use in multi-user mode.
    It has no admin check because the first "create user" experience
    happens right after enabling multi-user, before there are any
    other users with credentials.
    """
    if not data.username.strip():
        raise HTTPException(status_code=400, detail="Username cannot be empty")
    if len(data.password) < 6:
        raise HTTPException(status_code=400, detail="Password must be at least 6 characters")

    conn = get_connection()
    try:
        dup = conn.execute(
            "SELECT id FROM users WHERE username = ?", (data.username.strip(),)
        ).fetchone()
        if dup:
            raise HTTPException(status_code=409, detail="Username already taken")

        cursor = conn.execute(
            "INSERT INTO users (username, password) VALUES (?, ?)",
            (data.username.strip(), _hash_password(data.password)),
        )
        user_id = cursor.lastrowid

        token = _create_session(user_id, conn=conn)
        conn.commit()
        return {"token": token, "username": data.username.strip()}
    finally:
        conn.close()


@router.get("/auth/is-admin")
async def is_admin(authorization: str = Header(None)):
    """Check if the currently authenticated user is the admin (user id = 1).

    Admin status is determined solely by user ID — the first user created
    by the system is always user 1, and there is no mechanism to change this.
    """
    try:
        user_id = _get_user_id(authorization)
    except HTTPException:
        return {"isAdmin": False}

    return {"isAdmin": user_id == 1}


@router.get("/auth/has-user")
async def has_user():
    """Check if any admin user exists.

    Since the admin user (id=1) is auto-created on first run by
    ``init_db()``, this always returns ``{hasUser: true}``.
    """
    return {"hasUser": True}


@router.get("/auth/multi-user-mode")
async def get_multi_user_mode():
    """Get whether multi-user mode is currently enabled.

    When disabled (default), the app auto-logs in as admin and skips
    the login page. When enabled, a login page is displayed and users
    must authenticate with a username and password.
    """
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT value FROM settings WHERE key = 'multi_user_mode'"
        ).fetchone()
        return {"multiUserMode": row is not None and row["value"] == "true"}
    finally:
        conn.close()


@router.get("/auth/show-welcome")
async def get_show_welcome():
    """Get whether the login page should be shown.

    Mirrors the multi_user_mode setting. The frontend uses this to
    decide whether to show the login page or auto-navigate straight
    to the dashboard.
    """
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT value FROM settings WHERE key = 'multi_user_mode'"
        ).fetchone()
        show = row is not None and row["value"] == "true"
        return {"showLoginPage": show}
    finally:
        conn.close()


@router.put("/auth/multi-user-mode")
async def set_multi_user_mode(data: MultiUserModeRequest, authorization: str = Header(None)):
    """Enable or disable multi-user mode (admin only).

    **Enabling**: Requires admin authentication. The provided password
    is either used to verify the existing admin password, or set as the
    initial admin password if none was set before.

    **Disabling**: Requires admin auth + current admin password verification.
    """
    user_id = _get_user_id(authorization)
    if user_id != 1:
        raise HTTPException(status_code=403, detail="Only admin can change multi-user mode")

    conn = get_connection()
    try:
        if data.enabled:
            if not data.password:
                raise HTTPException(status_code=400, detail="Password is required to enable multi-user mode")
            if len(data.password) < 6:
                raise HTTPException(status_code=400, detail="Password must be at least 6 characters")

            admin_row = conn.execute(
                "SELECT password FROM users WHERE id = 1"
            ).fetchone()

            if admin_row and admin_row["password"]:
                # Admin already has a password — verify current password
                if not _verify_password(data.password, admin_row["password"]):
                    raise HTTPException(status_code=403, detail="Incorrect password")
            else:
                # First time — set the password
                conn.execute(
                    "UPDATE users SET password = ? WHERE id = 1",
                    (_hash_password(data.password),),
                )
                conn.execute(
                    "INSERT OR REPLACE INTO settings (key, value) VALUES ('password_hash', ?)",
                    (_hash_password(data.password),),
                )

            conn.execute(
                "INSERT OR REPLACE INTO settings (key, value) VALUES ('multi_user_mode', 'true')",
            )
            conn.commit()
            return {"multiUserMode": True}
        else:
            if not data.password:
                raise HTTPException(status_code=400, detail="Password is required to disable multi-user mode")

            admin_row = conn.execute(
                "SELECT password FROM users WHERE id = 1"
            ).fetchone()
            if admin_row and admin_row["password"]:
                if not _verify_password(data.password, admin_row["password"]):
                    raise HTTPException(status_code=403, detail="Incorrect password")

            conn.execute(
                "INSERT OR REPLACE INTO settings (key, value) VALUES ('multi_user_mode', 'false')",
            )
            conn.commit()
            return {"multiUserMode": False}
    finally:
        conn.close()


@router.get("/auth/auto-login")
async def auto_login():
    """Auto-login as the admin user (only works when multi-user mode is disabled).

    When multi-user mode is off, there is no login page — the frontend
    calls this on mount to get a session token for the admin user (id=1).
    If no session exists, one is created. If multi-user mode is on, this
    endpoint returns a 403 and the frontend must show the login form instead.
    """
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT value FROM settings WHERE key = 'multi_user_mode'"
        ).fetchone()
        if row and row["value"] == "true":
            raise HTTPException(status_code=403, detail="Multi-user mode is enabled, use login instead")

        user_row = conn.execute(
            "SELECT id, username FROM users WHERE id = 1"
        ).fetchone()
        if not user_row:
            raise HTTPException(status_code=500, detail="Admin user not found")

        token = _create_session(user_row["id"], conn=conn)
        conn.commit()
        return {"token": token, "username": user_row["username"]}
    finally:
        conn.close()


