@router.get("/settings/user")
async def get_user(authorization: str = Header(None)):
    """Get the username of the currently authenticated user."""
    user_id = _get_user_id(authorization)
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT username FROM users WHERE id = ?", (user_id,)
        ).fetchone()
        return {"username": row["username"] if row else "pilot"}
    finally:
        conn.close()


@router.put("/settings/username")
async def update_username(data: UsernameUpdate, authorization: str = Header(None)):
    """Update the username for the authenticated user."""
    user_id = _get_user_id(authorization)
    if not data.username.strip():
        raise HTTPException(status_code=400, detail="Username cannot be empty")
    if len(data.username) > 100:
        raise HTTPException(status_code=400, detail="Username too long")

    conn = get_connection()
    try:
        conn.execute(
            "UPDATE users SET username = ? WHERE id = ?",
            (data.username.strip(), user_id),
        )
        conn.execute(
            "INSERT OR REPLACE INTO settings (key, value) VALUES ('username', ?)",
            (data.username.strip(),),
        )
        conn.commit()
        return {"username": data.username.strip()}
    finally:
        conn.close()


@router.put("/settings/password")
async def change_password(data: PasswordUpdate, authorization: str = Header(None)):
    """Change the password for the authenticated user.

    Requires the current password for verification. The new password
    must be at least 6 characters long.
    """
    user_id = _get_user_id(authorization)
    if len(data.new_password) < 6:
        raise HTTPException(status_code=400, detail="Password must be at least 6 characters")

    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT password FROM users WHERE id = ?", (user_id,)
        ).fetchone()
        if not row:
            raise HTTPException(status_code=404, detail="User not found")
        if not _verify_password(data.current_password, row["password"]):
            raise HTTPException(status_code=403, detail="Current password is incorrect")

        conn.execute(
            "UPDATE users SET password = ? WHERE id = ?",
            (_hash_password(data.new_password), user_id),
        )
        conn.execute(
            "INSERT OR REPLACE INTO settings (key, value) VALUES ('password_hash', ?)",
            (_hash_password(data.new_password),),
        )
        conn.commit()
        return {"status": "ok"}
    finally:
        conn.close()


# ── Currency endpoints ──


@router.get("/currency/thresholds")
async def get_currency_thresholds(authorization: str = Header(None)):
    """Get currency thresholds for the authenticated user.

    Thresholds define the minimum number of events (e.g. landings,
    instrument approaches) required within a rolling time window
    to maintain currency in each category.

    Returns a dict keyed by ``category_id`` with ``minCount`` and
    ``daysWindow`` values.
    """
    user_id = _get_user_id(authorization)
    conn = get_connection()
    try:
        rows = conn.execute(
            "SELECT category_id, min_count, days_window FROM currency_thresholds WHERE user_id = ?",
            (user_id,),
        ).fetchall()
        thresholds: dict[str, dict[str, int]] = {}
        for row in rows:
            thresholds[row["category_id"]] = {
                "minCount": row["min_count"],
                "daysWindow": row["days_window"],
            }
        return {"thresholds": thresholds}
    finally:
        conn.close()


@router.put("/currency/thresholds")
async def save_currency_thresholds(
    data: CurrencyThresholdsSaveRequest,
    authorization: str = Header(None),
):
    """Save currency thresholds for the authenticated user.

    Uses ``INSERT OR REPLACE`` so that calling this endpoint multiple
    times is idempotent. Sending an empty list clears all thresholds.
    """
    user_id = _get_user_id(authorization)
    conn = get_connection()
    try:
        for entry in data.thresholds:
            conn.execute(
                """INSERT OR REPLACE INTO currency_thresholds
                   (user_id, category_id, min_count, days_window)
                   VALUES (?, ?, ?, ?)""",
                (user_id, entry.category_id, entry.min_count, entry.days_window),
            )
        conn.commit()
        return {"status": "ok"}
    finally:
        conn.close()


