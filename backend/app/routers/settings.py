@router.get("/settings/visibility")
async def get_visibility(authorization: str = Header(None)):
    """Get the page and column visibility configuration for the authenticated user.

    Visibility settings control which optional pages appear in the
    navigation bar and which columns appear in the Logbook and EntryForm.
    Both values are stored as JSON-encoded strings.
    """
    user_id = _get_user_id(authorization)
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT page_visibility, column_visibility FROM user_visibility WHERE user_id = ?",
            (user_id,),
        ).fetchone()
        if row:
            return {
                "pageVisibility": row["page_visibility"],
                "columnVisibility": row["column_visibility"],
            }
        return {"pageVisibility": "{}", "columnVisibility": "{}"}
    finally:
        conn.close()


@router.put("/settings/visibility")
async def save_visibility(
    data: VisibilitySaveRequest,
    authorization: str = Header(None),
):
    """Save the page and column visibility configuration for the authenticated user."""
    user_id = _get_user_id(authorization)
    conn = get_connection()
    try:
        conn.execute(
            """INSERT OR REPLACE INTO user_visibility
               (user_id, page_visibility, column_visibility)
               VALUES (?, ?, ?)""",
            (user_id, data.page_visibility, data.column_visibility),
        )
        conn.commit()
        return {"status": "ok"}
    finally:
        conn.close()
