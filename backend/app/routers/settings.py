"""
User settings, authentication, and preferences API routes.

This module bundles several logical groups of endpoints on a single FastAPI
router (``/api/settings``) for simplicity, even though they cover distinct
domains:

  **Auth** (``/api/auth/*``)
    - POST   /auth/login             — Login with username & password.
    - POST   /auth/create-user       — Create additional user (multi-user mode).
    - GET    /auth/is-admin          — Check if current user is admin (id=1).
    - GET    /auth/has-user          — Check if any admin exists (always True).
    - GET    /auth/multi-user-mode   — Get whether multi-user is enabled.
    - GET    /auth/show-welcome      — Whether login page should be shown.
    - PUT    /auth/multi-user-mode   — Enable/disable multi-user (admin only).
    - GET    /auth/auto-login        — Auto-login as admin (single-user mode).

  **Settings** (``/api/settings/*``)
    - GET    /settings/user          — Get current username.
    - PUT    /settings/username      — Update username.
    - PUT    /settings/password      — Change password.
    - GET    /settings/visibility    — Get page & column visibility config.
    - PUT    /settings/visibility    — Save page & column visibility.

  **Currency** (``/api/currency/*``)
    - GET    /currency/thresholds    — Get currency thresholds.
    - PUT    /currency/thresholds    — Save currency thresholds.

All protected endpoints require ``Authorization: Bearer <token>``.
"""

import hashlib
import os
import secrets
from fastapi import APIRouter, HTTPException, Header, Body
from pydantic import BaseModel, Field

from app.database import get_connection

router = APIRouter()


# ═══════════════════════════════════════════════
# Password Hashing Helpers
# ═══════════════════════════════════════════════

def _hash_password(password: str) -> str:
    """Hash a password using SHA-256 with a random 32-byte salt.

    Format: ``<salt_hex>:<sha256_hex>``
    The salt is stored alongside the hash so we can verify later.
    This is **not** a production-grade password hashing scheme (use bcrypt
    or argon2 for that), but it provides basic protection for a self-hosted
    personal logbook where the threat model is casual access.
    """
    salt = os.urandom(32)
    pwd = salt + password.encode("utf-8")
    hashed = hashlib.sha256(pwd).hexdigest()
    return salt.hex() + ":" + hashed


def _verify_password(password: str, stored: str) -> bool:
    """Verify a plaintext password against a stored ``salt:hash`` string."""
    try:
        salt_hex, hashed = stored.split(":", 1)
        salt = bytes.fromhex(salt_hex)
        pwd = salt + password.encode("utf-8")
        return hashlib.sha256(pwd).hexdigest() == hashed
    except (ValueError, AttributeError):
        return False


# ═══════════════════════════════════════════════
# Session helpers (shared with flights router)
# ═══════════════════════════════════════════════

def _get_user_id(authorization: str) -> int:
    """Extract the authenticated user's ID from the Authorization header.

    Identical to the version in ``flights.py``; duplicated here to avoid
    a circular import. Looks up the Bearer token in the ``sessions`` table.
    """
    if not authorization:
        raise HTTPException(status_code=401, detail="Authorization required")
    token = authorization.replace("Bearer ", "").strip()
    conn = get_connection()
    try:
        row = conn.execute(
            "SELECT user_id FROM sessions WHERE token = ?", (token,)
        ).fetchone()
        if not row:
            raise HTTPException(status_code=401, detail="Invalid or expired token")
        return row["user_id"]
    finally:
        conn.close()


def _create_session(user_id: int, conn=None) -> str:
    """Create a session token for the given user ID.

    If an existing connection is provided (e.g. from the caller's
    ``with get_connection()`` block), we reuse it so that the token
    creation can be part of a larger transaction. Otherwise we create
    our own connection.

    The token itself is a cryptographically random 64-character hex
    string generated by ``secrets.token_hex(32)``.
    """
    token = secrets.token_hex(32)
    if conn is not None:
        conn.execute(
            "INSERT OR REPLACE INTO sessions (token, user_id) VALUES (?, ?)",
            (token, user_id),
        )
    else:
        own_conn = get_connection()
        try:
            own_conn.execute(
                "INSERT OR REPLACE INTO sessions (token, user_id) VALUES (?, ?)",
                (token, user_id),
            )
            own_conn.commit()
        finally:
            own_conn.close()
    return token


